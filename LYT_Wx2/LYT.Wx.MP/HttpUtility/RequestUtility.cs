        public static async Task<string> HttpPostAsync(string url, object datas = null)
        {
           
            string result = "";
            HttpWebRequest req = (HttpWebRequest)WebRequest.Create(url);
            req.Method = "POST";          
            req.ContentType = "application/json";
            #region 
           
            byte[] data = Encoding.UTF8.GetBytes(JsonHelper.Serialize(datas));
            req.ContentLength = data.Length;
            using (Stream reqStream = await req.GetRequestStreamAsync())
            {
                await reqStream.WriteAsync(data, 0, data.Length);
                reqStream.Close();
            }
            #endregion         
            HttpWebResponse resp = (HttpWebResponse)(await req.GetResponseAsync());

            Stream stream = resp.GetResponseStream();
            //获取响应内容  
            using (StreamReader reader = new StreamReader(stream, Encoding.UTF8))
            {
                result = await reader.ReadToEndAsync();
            }
            return result;
        }
        /// <summary>
        /// 使用Post方法获取字符串结果，常规提交
        /// </summary>
